"""
Video: youtube.com/shorts/c7qqaNUYkRI

El método pop en Python:

Este método es utilizado para quitarle un elemento a una lista y retornar ese elemento. Si ningún índice es especificado, le quita el último elemento a una lista y lo retorna. Considere el ejemplo que ve en pantalla.
"""

letras = ["a", "b", "c"]

ultima_letra = letras.pop()
print(ultima_letra)

primera_letra = letras.pop(0)
print(primera_letra)
print(letras)
