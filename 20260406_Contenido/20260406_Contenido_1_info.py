"""
Video: youtube.com/shorts/JLY0ujKn5UY

Cómo convertir una cadena a mayúsculas con Python:

Podemos utilizar el método upper(), que es derivado de uppercase que signfica mayúsculas, para convertir todos los caracteres a mayúsculas. Si queremos que solamente el primer carácter sea mayúscula, usamos el método capitalize(). Y si queremos que cada palabra empiece con una letra mayúscula, usamos el método title(), que signfica título. Considere los ejemplos que ve en pantalla.
"""

cadena = "hola a todos"
print(cadena.upper())
print(cadena.capitalize())
print(cadena.title())
