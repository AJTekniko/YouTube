"""
Video: youtube.com/shorts/dXzb8Iu3-vw

Las asignaciones aumentadas en Python:

Estas asignaciones le pueden aplicar una operación aritmética a una variable y almacenar el resultado en la misma variable. Considere los ejemplos que ve en pantalla.
"""

x = 10
print(f"El valor original de x es {x}")

x += 5
print(f"x += 5 produce {x}")

x -= 3
print(f"x -= 3 produce {x}")

x *= 2
print(f"x *= 2 produce {x}")

x /= 3
print(f"x /= 3 produce {x}")

x %= 3
print(f"x %= 3 produce {x}")

x **= 4
print(f"x **= 4 produce {x}")