"""
Video: youtube.com/shorts/B9XKnV7WUAw

La función personalizada en Python:

Este tipo de función es utilizada para procesar argumentos y producir valores de retorno. Se define usando la palabra clave def, un nombre que describe lo que hace la función, sus parámetros en un par de paréntesis, y dos puntos. Luego, después de una indentación, se escribe lo que queremos que haga la función. Para llamar la función, usamos el nombre de la función seguido por un par de paréntesis que contiene los argumentos apropiados. Considere el ejemplo que ve en pantalla.
"""

def obtener_suma(num_1, num_2):
    return num_1 + num_2

resultado = obtener_suma(2, 5)
print(resultado)