"""
Video: youtube.com/shorts/XRCVEatafNw

El método replace en Python:

Este método crea una nueva cadena en la que reemplaza una subcadena con otra. Por medio del tercer argumento, se puede especificar la cantidad de veces que necesita ser reemplazada. Si el tercer argumento es omitido, entonces todos los instantes en los que se encuentra la subcadena indicada son reemplazados por otra. Considere el ejemplo que ve en pantalla.
"""

cadena = "Mi perro es un perro que se llama 'perro'."

print(cadena.replace("perro", "mascota", 1))
